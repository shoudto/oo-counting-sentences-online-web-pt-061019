require 'pry'

class String

  def sentence?
    if self.end_with?(".")
      true
    else
      false
    end
  end

  def question?
    if self.end_with?("?")
      true
    else
      false
    end
  end

  def exclamation?
    if self.end_with?("!")
      true
    else
      false
    end
  end

# "one. two. three?"

  def count_sentences
    self.split(/\.|\?|\!/).delete_if {|x| x == ""}.count 
  end
end
